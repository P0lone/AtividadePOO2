public class Livro {
    String titulo;
    String autor;
    String editora;
    String categoria;
    boolean disponibilidade;

    public Livro(String titulo, String autor, String editora, String categoria, boolean disponibilidade){
        this.titulo = titulo;
        this.autor = autor;
        this.editora = editora;
        this.categoria = categoria;
        this.disponibilidade = disponibilidade;
    }

}
