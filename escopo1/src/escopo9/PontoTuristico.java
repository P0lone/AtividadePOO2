package escopo9;

public class PontoTuristico {
    String nome;
    String pais;
    String estado;
    String cidade;
    String informacoes;
    public PontoTuristico(String nome, String pais, String estado, String cidade, String informacoes){
        this.nome = nome;
        this.pais = pais;
        this.estado = estado;
        this.cidade = cidade;
        this.informacoes = informacoes;
    }
}
