package escopo5;

public class Advogado {
    String nome;
    String email;
    String cpf;
    String horasTrabalhadas;
    public Advogado(String nome, String email, String cpf, String horasTrabalhadas){
        this.nome = nome;
        this.email = email;
        this.cpf = cpf;
        this.horasTrabalhadas = horasTrabalhadas;
    }
}
